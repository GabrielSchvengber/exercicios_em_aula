
a=input('Digite algo: ')

print('O tipo primitivo deste valor é: ', type(a))
print('Só tem espacos ', a.isspace())
print('É um numero', a.isnumeric())
print('É alfabético ', a.isalpha())
print('É alfanumérica ', a.isalnum())
print('Está em maiuscula ', a.isupper())
print('ELa está em minuscula ', a.islower())
print('Está capitalizada ', a.istitle())
